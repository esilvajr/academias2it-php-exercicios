<?php

// Crie uma classe Math que faça as quatro (4) operações básicas: Soma, Subtração, Multiplicação e Divisão entre dois valores.
class Math
{
    public function sum($a, $b)
    {
        return $a + $b;
    }

    public function sub($a, $b)
    {
        return $a - $b;
    }

    public function multiply($a, $b)
    {
        return $a * $b;
    }

    public function divide($a, $b)
    {
        return $a / $b;
    }
}